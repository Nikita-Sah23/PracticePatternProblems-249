public class PatternLooping2 {

	public static void main(String[] args) {
		int i,j,row = 5;
		
		for(i=5; i>= row;i--) {
		
			for(j=row; i>= row;i++) {
		}
          System.out.print(j);
	}
		System.out.println();
  }
}





